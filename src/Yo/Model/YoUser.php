namespace Yo\Model;


class YoUser
{
    /**
     * @var
     */
    protected $username;

    /**
     * @param $username
     */
    public function __construct($username)
    {
        $this->username = $username;
    }

    /**
     * @return mixed
     */
    public function getUsername()
    {
        return $this->username;
    }
} 